#!/usr/bin/javascript

// Mengulangi pesan 5 kali dengan for loop
for (let i = 0; i < 5; i++) {
  console.log("Nama Saya Adalah Kevin-" + (i+1));
}

// Mengulangi pesan menggunakan while loop
let counter = 0;
while (counter < 5) {
  console.log("Nama Saya Adalah Kevin-" + (counter+1));
  counter++;
}
